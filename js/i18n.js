function getNestedValue(obj, path) {
  return path.split(".").reduce((acc, part) => acc && acc[part], obj);
}

function setFlagButtons(lang) {
  const nextLang = lang === "en" ? "es" : "en";

  const config = {
    en: { icon: "fi-us", text: "EN" },
    es: { icon: "fi-mx", text: "ES" },
  };

  const current = config[nextLang];

  const ids = [
    ["langToggleIcon", "langToggleText"],
    ["mobileLangToggleIcon", "mobileLangToggleText"],
  ];

  ids.forEach(([iconId, textId]) => {
    const icon = document.getElementById(iconId);
    const text = document.getElementById(textId);

    if (icon) {
      icon.className = `fi ${current.icon}`;
    }

    if (text) {
      text.textContent = current.text;
    }
  });
}

function applyTranslations(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = getNestedValue(translations[lang], key);

    if (value) {
      element.textContent = value;
    }
  });

  setFlagButtons(lang);
}

function toggleLanguage() {
  const currentLang = localStorage.getItem("siteLanguage") || "en";
  const newLang = currentLang === "en" ? "es" : "en";
  localStorage.setItem("siteLanguage", newLang);
  applyTranslations(newLang);
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("siteLanguage") || "en";
  applyTranslations(savedLang);

  const langToggle = document.getElementById("langToggle");
  const mobileLangToggle = document.getElementById("mobileLangToggle");

  if (langToggle) {
    langToggle.addEventListener("click", toggleLanguage);
  }

  if (mobileLangToggle) {
    mobileLangToggle.addEventListener("click", toggleLanguage);
  }
});